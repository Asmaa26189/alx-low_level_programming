#include <stdio.h>
#include <stdlib.h>
#include "3-calc.h"
/**
 * op_add - add
 * @a: int
 * @b: int
 * Return: sum
 */
int op_add(int a, int b)
{
	return (a + b);
}
/**
 * op_sub - subtract
 * @a: int
 * @b: int
 * Return: difference
 */
int op_sub(int a, int b)
{
	return (a - b);
}
/**
 * op_mul - multiplie
 * @a: int
 * @b: int
 * Return: times
 */
int op_mul(int a, int b)
{
	return (a * b);
}
/**
 * op_div - divide
 * @a: int
 * @b: int
 * Return: divide
 */
int op_div(int a, int b)
{
	return (a / b);
}
/**
 * op_mod - mod
 * @a: int
 * @b: int
 * Return: mod
 */

int op_mod(int a, int b)
{
	return (a % b);
}
